// Generates worker/site-context.ts (gitignored) from src/data/cv.md plus
// site facts, so the /api/chat assistant answers from current content.
// Runs via the `prebuild` npm hook — works in CI (no Chrome needed).
import { readFile, writeFile, mkdir } from 'node:fs/promises';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const cv = await readFile(join(root, 'src/data/cv.md'), 'utf8');

const context = `# h1sort.com — site knowledge

## Site map
- / — landing (animated sorting-algorithm hero)
- /cv/ — animated CV slide deck; PDF download at /cv.pdf
- /writing/ — writing hub (blog + research write-ups; first posts coming soon)
- /teaching/ — Real-Time Data Processing course + AI-enablement workshops
- /contact/ — all contact channels

## Contact
- Email: carlos@h1sort.com
- GitHub: github.com/haro-ca
- LinkedIn: linkedin.com/in/h1sort
- X: x.com/h1sort
- Location: Mexico City, Mexico

## Teaching
Real-Time Data Processing — a 12-lesson hands-on course (3h per lesson, 1:2
theory-to-hands-on) from a single Postgres node to a full streaming pipeline:
OLTP → CDC → Kafka → stream processing → real-time OLAP → API.
Stack: PostgreSQL, CockroachDB, DuckDB, Apache Kafka, PySpark, Apache Flink,
ClickHouse, FastAPI, Schema Registry, Grafana.
Repo: github.com/haro-ca/real-time-data-processing-class

## Curriculum Vitae (full)
${cv}
`;

await mkdir(join(root, 'worker'), { recursive: true });
await writeFile(
  join(root, 'worker/site-context.ts'),
  `// GENERATED by scripts/build-chat-context.mjs — do not edit.\nexport const SITE_CONTEXT = ${JSON.stringify(context)};\n`,
);
console.log('chat-context: wrote worker/site-context.ts');
